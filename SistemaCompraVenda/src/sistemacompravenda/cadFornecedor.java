        f.setEmail(txtEmail.getText());
        f.setCnpj(txtCNPJ.getText());
        f.setCidade(txtCidade.getText());
        f.setCep(txtCEP.getText());
        f.setUf(cmbUF.getSelectedItem().toString());
        f.setRua(txtRua.getText());
