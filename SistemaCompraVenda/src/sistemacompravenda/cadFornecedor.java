    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
        FornecedorDAO fDAO = new FornecedorDAO();
        List<Fornecedor> fornecedores = fDAO.listarFornecedores();
        
        DefaultListModel<Fornecedor> model = new DefaultListModel<>();
        fornecedores.forEach((f) -> {
            model.addElement(f);
        });
        lstFornecedor.setModel(model);
    }//GEN-LAST:event_btnListarActionPerformed

    private void lstFornecedorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstFornecedorMouseClicked
        Fornecedor f = lstFornecedor.getSelectedValue();
        if(f != null){
            txtID.setText(String.valueOf(f.getId()));
            txtNome.setText(f.getNome());
            txtNomeFantasia.setText(f.getNomeFantasia());
            txtEmail.setText(f.getEmail());
            txtTelefone.setText(f.getTelefone());
            txtCidade.setText(f.getCidade());
            txtCEP.setText(f.getCep());
            txtRua.setText(f.getRua());
            txtBairro.setText(f.getBairro());
            txtNumero.setText(String.valueOf(f.getNumero()));
            txtComplemento.setText(f.getComplemento());
            txtCNPJ.setText(f.getCnpj());
            cmbUF.setSelectedItem(f.getUf().trim());
        }
    }//GEN-LAST:event_lstFornecedorMouseClicked

