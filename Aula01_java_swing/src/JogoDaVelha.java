   private void verificaVencedor() {
    String vencedor = null;

    // Verifica linhas e colunas
    for (int i = 0; i < 3; i++) {
        // Linhas
        if (!btn[i][0].getText().equals("") &&
            btn[i][0].getText().equals(btn[i][1].getText()) &&
            btn[i][1].getText().equals(btn[i][2].getText())) {
                vencedor = btn[i][0].getText();
        }

        // Colunas
        if (!btn[0][i].getText().equals("") &&
            btn[0][i].getText().equals(btn[1][i].getText()) &&
            btn[1][i].getText().equals(btn[2][i].getText())) {
                vencedor = btn[0][i].getText();
        }
    }

    // Diagonal principal
    if (!btn[0][0].getText().equals("") &&
        btn[0][0].getText().equals(btn[1][1].getText()) &&
        btn[1][1].getText().equals(btn[2][2].getText())) {
            vencedor = btn[0][0].getText();
    }

    // Diagonal secundÃ¡ria
    if (!btn[0][2].getText().equals("") &&
        btn[0][2].getText().equals(btn[1][1].getText()) &&
        btn[1][1].getText().equals(btn[2][0].getText())) {
            vencedor = btn[0][2].getText();
    }
    if (vencedor != null) {
        javax.swing.JOptionPane.showMessageDialog(this, "Jogador '" + vencedor + "' venceu!");   
    }
}


    
