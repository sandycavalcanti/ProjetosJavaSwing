    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String sexo;
        if(rdbMasc.isSelected()){
            sexo = "M";
            JOptionPane.showMessageDialog(null, "Sexo selecionado: "+sexo, "Alerta!", JOptionPane.OK_OPTION);
        }
        else if(rdbFem.isSelected()){
            sexo = "F";
            JOptionPane.showMessageDialog(null, "Sexo selecionado: "+sexo, "Alerta!", JOptionPane.OK_OPTION);
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um sexo", "Erro!", JOptionPane.OK_OPTION);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

